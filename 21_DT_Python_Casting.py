# ===========================================
# Python Casting Example
# int() , float() , str()
# ===========================================

# -------------------------------
# Original Value
# -------------------------------

number = 25

print("Original Value :", number)
print("Original Data Type :", type(number))
print()

# -------------------------------
# int()
# -------------------------------

x = int(25.89)

print("Using int()")
print("Value :", x)
print("Data Type :", type(x))
print()

# -------------------------------
# float()
# -------------------------------

y = float(number)

print("Using float()")
print("Value :", y)
print("Data Type :", type(y))
print()

# -------------------------------
# str()
# -------------------------------

z = str(number)

print("Using str()")
print("Value :", z)
print("Data Type :", type(z))
print()